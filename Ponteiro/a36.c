    *p3 = a * b + *p1;//p3 = 3 * 2 + 5 = 11; b = 11

    b = *p3 * a + *p1;//b = 11 * 3 + 5 = 38; p3 = 38

    a++; //a = 4

    *p1 = a + b;//p1 = 4 + 38 = 42; p1 = p2 = 42; c = 42; 

    c++;//c = 43

    printf("a = %d b = %d c = %d p1 = %d p2 = %d p3 = %d", a, b, c, *p1, *p2, *p3);
